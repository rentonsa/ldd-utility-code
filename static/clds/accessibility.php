        <h2>Technical information about this website’s accessibility</h2>
        <p>The University of Edinburgh is committed to making its website accessible, in accordance with the Public Sector Bodies (Websites and Mobile Applications) (No. 2) Accessibility Regulations 2018.</p>
        <p>When an issue is identified with the website we will liaise with the developers to highlight the issue and seek a solution. While investigation is underway, we will ensure reasonable adjustments are in place to make sure no user is disadvantaged</p>
        <p>This website is partially compliant with the Web Content Accessibility Guidelines 2.1 AA standard, due to the non-compliances listed below.</p>
        <p>The full guidelines are available at:</p>
        <a href="https://www.w3.org/TR/WCAG21/" alt-text="Web Content Accessibility Guidelines version 2.1"  title="Web Content Accessibility Guidelines version 2.1" target="_blank">Web Content Accessibility Guidelines version 2.1</a>
        <h3>Non accessible content</h3>
        <p>The content listed below is non-accessible for the following reasons.</p>
        <h4>Noncompliance with the accessibility regulations</h4>
        <p>The following items to not comply with the WCAG 2.1 AA success criteria:</p>
        <ul>
            <li>Information is conveyed as an image of text rather than as text itself so that it's not compatible with screen readers and other assistive technology<br>
                <a href="https://www.w3.org/TR/WCAG21/#images-of-text" alt-text="1.4.5 - Images of text" title="1.4.5 - Images of text" target="_blank">1.4.5 - Images of text</a></li>
            <li>Most tooltips disappear as soon as the cursor moves. Also tooltips are not always present for all icons and images.<br>
                <a href="https://www.w3.org/TR/WCAG21/#content-on-hover-or-focus" alt-text="1.4.13 - Content on Hover or Focus" title="1.4.13 - Content on Hover or Focus" target="_blank">1.4.13 - Content on Hover or Focus</a></li>
            <li>There may not be sufficient colour contrast between font and background colours especially where the text size is very small.<br>
                <a href="https://www.w3.org/TR/2008/REC-WCAG20-20081211/#visual-audio-contrast-contrast" alt-text="1.4.3 - Contrast (Minimum)" title="1.4.3 - Contrast (Minimum)" target="_blank">1.4.3 - Contrast (Minimum)</a></li>
            <li>Visual information to identify user interface components, such as keyboard focus, do not always have a sufficient contrast ratio<br>
                <a href="https://www.w3.org/TR/WCAG21/#non-text-contrast" alt-text="1.4.11 - Non-text contrast" title="1.4.11 - Non-text contrast" target="_blank">1.4.11 - Non-text contrast</a></li>
            <li>Some content cannot be presented without loss of information when magnified to the maximum browser level<br>
                <a href="https://www.w3.org/TR/WCAG21/#reflow" alt-text="1.4.10 - Reflow" title="1.4.10 - Reflow" target="_blank">1.4.10 - Reflow</a></li>
            <li>Not all non-text content has text alternatives<br>
                <a href="https://www.w3.org/TR/WCAG21/#text-alternatives" alt-text="1.1.1 – Non Text Content" title="1.1.1 – Non Text Content" target="_blank">1.1.1 – Non Text Content</a></li>
            <li>Not fully compatible with screen readers<br>
                <a href="https://www.w3.org/TR/WCAG21/#compatible" alt-text="4.1 - Compatible" title="4.1 - Compatible" target="_blank">4.1 - Compatible</a></li>
        </ul>
        <p>Unless specified otherwise a complete solution or significant improvement will be in place by September 2020.</p>
        <h2>How we tested this website</h2>
        <p>This website was last tested in November 2019. The website was tested by the University of Edinburgh’s Disability Information Officer on a PC using Internet Explorer (11.0.9600.19236) browser
        and also using the automated Webaim WAVE tool. Internet Explorer was chosen as the browser due to it being the most commonly used browser by disabled people as shown in the government survey:
        the Government Assistive Technology Browser Survey</p>
        <p>We tested:</p>
        <ul>
            <li>Spellcheck functionality</li>
            <li>Data validation</li>
            <li>Scaling using different screen resolutions</li>
            <li>Options to customise the interface (magnification, font, background colour etc.)</li>
            <li>Keyboard navigation</li>
            <li>Warning of links opening in a new tab or window</li>
            <li>Information conveyed in colour or sound only</li>
            <li>Flashing or scrolling text</li>
            <li>Operability if Javascript is disabled</li>
            <li>Use with screen reading software (eg. JAWS)</li>
            <li>Assistive Software such as TextHelp Read and Write, and Zoomtext</li>
            <li>Tooltips and text alternatives for any non-text content</li>
            <li>Time limits</li>
        </ul>
        <h2>What we're doing to improve accessibility</h2>
        <p>We will continue to work with developers to address these issues and deliver a solution or suitable workaround and correct issues directly. We hope to have the majority of issues resolved by September 2020.</p>
        <p>We will continue to monitor the website will carry out further accessibility testing if significant changes are made to the user interface or if a service user raises an issue.</p>
        <p>Whist we are in the process of resolving these accessibility issues we will of course ensure reasonable adjustments are in place to make sure no user is disadvantaged.</p>


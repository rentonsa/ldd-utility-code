<h2>How accessible this website is</h2>
<p>We know some parts of this website are not fully accessible:</p>
<ul>
    <li>Users may not be able to access all content by using the keyboard alone</li>
    <li>some parts may not be fully compatible with screen readers</li>
    <li>you may not be able to access all content by using the keyboard alone and it can be hard to tell where you have navigated too</li>
    <li>There is movement that cannot be stopped by the user</li>
    <li>not all images have alternative text</li>
    <li>tooltips are not enabled for all icons</li>
</ul>

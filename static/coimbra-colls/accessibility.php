<h1>Accessibility Statement for Coimbra Group Collections Website</h1>
<p>Website accessibility statement in line with Public Sector Body (Websites and Mobile Applications) (No. 2) Accessibility Regulations 2018</p>
<p><a href="https://collections.ed.ac.uk/coimbra-colls">Coimbra Group Collections</a> (<a href="https://collections.ed.ac.uk/coimbra-colls">https://collections.ed.ac.uk/coimbra-colls</a>) is a website hosted by Library and University Collections on behalf of the University of Edinburgh. It is a directory of collections held by Coimbra Group institutions and is a front-facing public website.</p>
<p>We want as many people as possible to be able to use this website. For example, that means you should be able to:</p>
<ul>
    <li>change colours, contrast levels and fonts</li>
    <li>zoom in up to 200% without the text spilling off the screen</li>
    <li>users will not be timed out of the website</li>
    <li>navigate most of the website using just a keyboard</li>
    <li>use the site with assistive technology, including TextHelp Read and Write and ZoomText</li>
    <li>listen to most of the website using a screen reader (including the most recent versions of Job Access with Speech (JAWS), NonVisual Desktop Access (NVDA) and VoiceOver)</li>
</ul>
<p>We&rsquo;ve also made the website text as simple as possible to understand.</p>

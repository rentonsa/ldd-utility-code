<h2>Technical information about this website&rsquo;s accessibility</h2>
<p>The University of Edinburgh is committed to making its websites and applications accessible, in accordance with the Public Sector Bodies (Websites and Mobile Applications) (No. 2) Accessibility Regulations 2018.</p>
<p>This website is partially compliant with the Web Content Accessibility Guidelines (WCAG) 2.1 AA standard, due to the non-compliances listed below.</p>
<p>The full guidelines are available at:</p>
<p><a href="https://www.w3.org/TR/WCAG21/">Web Content Accessibility Guidelines (WCAG) 2.1 AA standard</a></p>
<h3>Non accessible content</h3>
<p>Noncompliance with the accessibility regulations</p>
<p>The following items to not comply with the WCAG 2.1 AA success criteria:&nbsp;</p>
<ul>
    <li>Not all non-text content presented to users has alternative text</li>
    <ul>
        <li><a href="https://www.w3.org/TR/WCAG21/#non-text-content">1.1.1 - Non-text Content</a></li>
    </ul>
    <li>There may not be sufficient colour contrast between font and background colours, especially where the text size is very small</li>
    <ul>
        <li><a href="https://www.w3.org/TR/2008/REC-WCAG20-20081211/#visual-audio-contrast-contrast">1.4.3 - Contrast (Minimum)</a></li>
    </ul>
    <li>Visual information to identify user interface components, such as keyboard focus, do not always have a sufficient contrast ratio</li>
    <ul>
        <li><a href="https://www.w3.org/TR/WCAG21/#non-text-contrast">1.4.11 - Non-text contrast</a></li>
    </ul>
    <li>Most tooltips disappear as soon as the cursor moves. Also tooltips are not always present for all icons and images</li>
    <ul>
        <li><a href="https://www.w3.org/TR/WCAG21/#content-on-hover-or-focus">1.4.13 - Content on Hover or Focus</a></li>
    </ul>
    <li>It is not possible to use a keyboard to access all the content</li>
    <ul>
        <li><a href="https://www.w3.org/TR/WCAG21/#keyboard-accessible">2.1 - Keyboard accessible</a></li>
    </ul>
</ul>
<ul>
    <li>It is not always clear to tell where you have navigated to when you are using a keyboard
        <ul>
            <li><a href="https://www.w3.org/TR/WCAG21/#focus-visible">4.7 - Focus Visible</a></li>
        </ul>
    </li>
    <li>There is no specific help function section, including accessibility information</li>
</ul>
<ul>
    <ul>
        <li><a href="https://www.w3.org/TR/WCAG21/#help">3.3.5 - Help</a></li>
    </ul>
</ul>
<p>&nbsp;Unless specified otherwise a complete solution or significant improvement will be in place by March 2021 for the issues within our control.</p>
<h3>Disproportionate burden</h3>
<p>We are not currently claiming that any accessibility problems would be a disproportionate burden to fix.</p>
<h2>Content that&rsquo;s not within the scope of the accessibility regulations</h2>
<h3>Reproduction of Heritage Collections</h3>
<p>There are representations of <a href="https://collections.ed.ac.uk/coimbra-colls/search">the heritage collections and its institutional location</a> across the website. There is technical information about the images of these items, including titles and description. However, reproducing items within heritage collections would be <a href="https://www.legislation.gov.uk/uksi/2018/952/regulation/4/made">exempt from meeting the accessibility regulations</a>, but all effort has been made to make the reproduction of these heritage collections as accessible as possible.</p>
<h2>What we&rsquo;re doing to improve accessibility</h2>
<p>We will continue to work to address these issues and deliver a solution or suitable workaround. Where issues are under our control we will work to rectify to correct issues. We plan to resolve the issues that are within our control by March 2021.</p>
<p>We will carry out further accessibility testing if significant changes are made to the user interface or if a service user raises an issue.</p>
<p>While we are in the process of resolving these accessibility issues, or where we are unable to, we will ensure reasonable adjustments are in place to make sure no user is disadvantaged.</p>
<p>Preparation of this accessibility statement</p>
<p><strong>This statement was prepared on 11<sup>th</sup> June 2020. It was last reviewed on 14<sup>th</sup> September 2020</strong></p>
<p>This website was tested by the University of Edinburgh&rsquo;s Deputy Disability Information Officer on a PC using Internet Explorer (11.0.9600.19236) browser and also using the automated <a href="https://wave.webaim.org/">WebAIM WAVE tool</a>. Internet Explorer was chosen as the browser due to it being the most commonly used browser by disabled people as shown in the government survey: <a href="https://accessibility.blog.gov.uk/2016/11/01/results-of-the-2016-gov-uk-assistive-technology-survey/">Government Assistive Technology Browser Survey</a>. We tested the home page and a selection of pages throughout the site to ensure all the main features of the website were tested.</p>
<p>&nbsp;We tested:</p>
<ul>
    <li>Spellcheck functionality</li>
    <li>Scaling using different resolutions</li>
    <li>Options to customise the interface (magnification, font, background colour et. cetera)</li>
    <li>Keyboard navigation</li>
    <li>Data validation</li>
    <li>Warning of links opening in a new tab or window</li>
    <li>Information conveyed in colour or sound only</li>
    <li>Flashing or scrolling text</li>
    <li>Operability if JavaScript is disabled</li>
    <li>Use with screen reading software (eg. JAWS)</li>
    <li>Assistive Software (TextHelp Read and Write, ZoomText)</li>
    <li>Tooltips and text alternatives for any non-text content</li>
    <li>Time limits</li>
</ul>
